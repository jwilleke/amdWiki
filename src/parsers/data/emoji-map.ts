/** Regex matching :shortcode: patterns — used by convertEmojiShortcodes */
export const EMOJI_SHORTCODE_REGEX = /:([a-z0-9_+-]+):/g;

/**
 * Replace all :shortcode: occurrences in text with their Unicode emoji.
 * Unknown shortcodes are left unchanged.
 */
export function convertEmojiShortcodes(text: string): string {
  return text.replace(EMOJI_SHORTCODE_REGEX, (match, name: string) => EMOJI_MAP[name] ?? match);
}
